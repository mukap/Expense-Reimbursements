package com.pojo;

public class Employee {
	
	private int emp_id;
	private String emp_name;
	private String emp_pass;
	private String emp_email;
	private String emp_mob;
	private String emp_amount;
	private String emp_type;
	private String emp_status;

	public int getEmp_id() {
		return emp_id;
	}
	public void setEmp_id(int emp_id) {
		this.emp_id = emp_id;
	}
	public String getEmp_name() {
		return emp_name;
	}
	public void setEmp_name(String emp_name) {
		this.emp_name = emp_name;
	}
	public String getEmp_pass() {
		return emp_pass;
	}
	public void setEmp_pass(String emp_pass) {
		this.emp_pass = emp_pass;
	}
	public String getEmp_email() {
		return emp_email;
	}
	public void setEmp_email(String emp_email) {
		this.emp_email = emp_email;
	}
	
	public String getEmp_mob() {
		return emp_mob;
	}
	public void setEmp_mob(String emp_mob) {
		this.emp_mob = emp_mob;
	}
	public String getEmp_amount() {
		return emp_amount;
	}
	public void setEmp_amount(String emp_amount) {
		this.emp_amount = emp_amount;
	}
	public String getEmp_type() {
		return emp_type;
	}
	public void setEmp_type(String emp_type) {
		this.emp_type = emp_type;
	}
	public String getEmp_status() {
		return emp_status;
	}
	public void setEmp_status(String emp_status) {
		this.emp_status = emp_status;
	}

}
